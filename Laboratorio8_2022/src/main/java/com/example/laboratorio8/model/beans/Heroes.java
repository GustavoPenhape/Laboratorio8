package com.example.laboratorio8.model.beans;

public class Heroes {
    private int idHeroes;
    private String nombre;
    private String edad;
    private String genero;
    private String clase;
    private String nivel_inicial;
    private String ataque;
    private String pareja;

    public int getIdHeroes() {
        return idHeroes;
    }

    public void setIdHeroes(int idHeroes) {
        this.idHeroes = idHeroes;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEdad() {
        return edad;
    }

    public void setEdad(String edad) {
        this.edad = edad;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public String getClase() {
        return clase;
    }

    public void setClase(String clase) {
        this.clase = clase;
    }

    public String getNivel_inicial() {
        return nivel_inicial;
    }

    public void setNivel_inicial(String nivel_inicial) {
        this.nivel_inicial = nivel_inicial;
    }

    public String getAtaque() {
        return ataque;
    }

    public void setAtaque(String ataque) {
        this.ataque = ataque;
    }

    public String getPareja() {
        return pareja;
    }

    public void setPareja(String pareja) {
        this.pareja = pareja;
    }
}
